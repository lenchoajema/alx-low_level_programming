#include <stdio.h>
#include <stdlib.h>
#include <time.h>

/**
 * main - Entry point
 *
 * Return: Always 0 (Success)
 */

int main(void)
{
	int d, e;

	for (d = 0, e=1; d < 100; d++, e++)
	{
		putchar((e / 10) + '1');
		putchar((d % 10) + '1');
		if (d != e)
		{
			putchar(',');
			putchar(' ');
		}
	}
	putchar('\n');

	return (0);
}
